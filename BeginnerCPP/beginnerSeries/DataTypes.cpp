#include <iostream>

using namespace std;

int main() {

  // Integers:
  int number = 5;
  cout << "Here is an integer: " << number << "\n";

  // Floats & Doubles:
  float pi = 3.14f;
  double e = 2.71d;
  cout << "Pi: " << pi << ", e: " << e << "\n";
  
  // Characters:
  char grade = 'A';
  cout << "Letter grade: " << grade << "\n";
  
  // Booleans (bool):
  bool isPassing = True;
  cout << "This is a passing grade: " << isPassing << "\n";
  
  // Strings (strings of characters!):
  string myName = "Justin";
  cout << "My name is: " << myName << "\n";

  return 0;

}
